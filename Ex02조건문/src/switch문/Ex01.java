package switch문;

public class Ex01 {

	public static void main(String[] args) {
		int num = 1;
		
		switch (num) {
		case 1: 
			System.out.println("1입니다.");
			break;
		case 2: 
			System.out.println("2입니다.");
			break;
		case 3: 
			System.out.println("3입니다.");
			break;
		case 4: 
			System.out.println("4입니다.");
			break;
		case 5: 
			System.out.println("5입니다.");
			break;
		default:
			System.out.println("그 외의 숫자입니다.");
			break;
		}
		
	}

}
